const STORAGE_KEY = "agentchat.greetingIdx2";

function getPool(hour: number): string[] {
  if (hour >= 5 && hour < 12) return [...GREETINGS_MORNING, ...GREETINGS_ANY];
  if (hour >= 12 && hour < 18) return [...GREETINGS_DAY, ...GREETINGS_ANY];
  if (hour >= 18 && hour < 23) return [...GREETINGS_EVENING, ...GREETINGS_ANY];
  return [...GREETINGS_NIGHT, ...GREETINGS_ANY];
}

export function pickGreeting(name?: string): string {
  const hour = new Date().getHours();
  const pool = getPool(hour);
  const idx = Math.floor(Math.random() * pool.length);
  let phrase = pool[idx] ?? pool[0] ?? "Привет!";
  if (name) phrase = phrase.replace(/\{name\}/g, name);
  else phrase = phrase.replace(/,?\s*\{name\}/g, "").replace(/\{name\},?\s*/g, "");
  return phrase;
}
